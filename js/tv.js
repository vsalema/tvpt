var $tv = $('.tv'),
$screen = $('.screen-content'),
$btn = $('.js-shtd-btn1'),
videoHtml = '<iframe src="https://vsalema.github.io/tvi/" width="880" height="700" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen scrolling="no"></iframe>';

$btn.on('click', function () {
  if ($tv.addClass('_off')) {
    $tv.removeClass('_off');
    $screen.html(videoHtml);
  } else {
    $tv.addClass('_off');
    $screen.empty();
  }
});


